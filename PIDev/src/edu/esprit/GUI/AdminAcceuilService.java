    private void modifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modifierActionPerformed
        int row = table.getSelectedRow();
        String tableclick = (table.getModel().getValueAt(row, 0).toString());
        try {
            
            String a2 = txtlieux.getText();
            String a3;
            if (radiopension.isSelected()) {
                a3 = "pension";
            } else {
                a3 = "toilettage";
            }
            String a4 = txtinfos.getText();
            String a5 = txtprix.getText();
            String sql = "update services set Lieux='" + a2 + "' ,Type='" + a3 + "' ,Infos='" + a4 + "' ,Prix='" + a5 + "' where id_Service='" + tableclick + "' ";
            pre = MyConnection.getInstance().prepareStatement(sql);
            pre.execute();
            JOptionPane.showMessageDialog(null, "Modification réussite");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        Update();
        // TODO add your handling code here:
    }//GEN-LAST:event_modifierActionPerformed

    private void supprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_supprimerActionPerformed
        int p = JOptionPane.showConfirmDialog(null, "Voulez vous vraiment supprimer ce service", "Supprimé", JOptionPane.YES_NO_OPTION);
        if (p == 0) {
            int row = table.getSelectedRow();
            String tableclick = (table.getModel().getValueAt(row, 0).toString());
            String sql = "delete from services where id_Service='" + tableclick + "' ";
            try {
                pre = MyConnection.getInstance().prepareStatement(sql);
                pre.execute();
                JOptionPane.showMessageDialog(null, "Supprimé");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
            Update();
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_supprimerActionPerformed

    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        try {
            int row = table.getSelectedRow();
            String tableclick = (table.getModel().getValueAt(row, 0).toString());
            String sql = "select * from services where id_Service='" + tableclick + "' ";
            pre = MyConnection.getInstance().prepareStatement(sql);
            rs = pre.executeQuery();
            if (rs.next()) {
                
                String a2 = rs.getString("Lieux");
                txtlieux.setText(a2);
                String a = rs.getString("Type");
                if (a.equals("pension")) {
                    radiopension.setSelected(true);
                } else {
                    radiotoilettage.setSelected(true);
                }
                String a3 = rs.getString("Infos");
                txtinfos.setText(a3);
                String a4 = rs.getString("Prix");
                txtprix.setText(a4);


            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_tableMouseClicked

