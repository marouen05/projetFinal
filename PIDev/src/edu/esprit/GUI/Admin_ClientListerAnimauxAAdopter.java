    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here:
        AnimalAAdopter an=new AnimalAAdopter();
        AnimalAAdopterDAO anDAO= new AnimalAAdopterDAO();
        ClientDAO clDAO = new ClientDAO();
        int row= jTable2.getSelectedRow();
        String tableclick= (jTable2.getModel().getValueAt(row, 0).toString());
        an= anDAO.findDepotById(Integer.parseInt(tableclick));
        tftype.setText(an.getType());
        tfespece.setText(an.getEspece());
        tfcouleur.setText(an.getCouleur());
        tfsexe.setText(an.getSexe());
        tfetat.setText(an.getEtat());
        tfdescription.setText(an.getDescription());
        tfville.setText(an.getNom());
        tfregion.setText(an.getAge()+"");
        tfmail.setText(clDAO.findClientbyid(an.getId_client()).getEmail());

        try {
            byte[] imagedata = an.getPhoto();
            format = new ImageIcon(imagedata);
            image.setIcon(format);

        } catch (Exception e) {
        }
}//GEN-LAST:event_jTable2MouseClicked

